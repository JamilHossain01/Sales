Future<void> _setupFirebaseMessaging() async {
  try {
    FirebaseMessaging messaging = FirebaseMessaging.instance;

    // ‚úÖ Request notification permission
    await messaging.requestPermission();

    // ‚úÖ Get and save FCM Token
    final token = await messaging.getToken();
    if (token != null) {
      print("üì≤ FCM Token: $token");
      await LocalStorage.saveData(key: AppConstant.fcmToken, data: token);
    }

    // ‚úÖ Listen for token refresh
    FirebaseMessaging.instance.onTokenRefresh.listen((newToken) async {
      print("‚ôª Token refreshed: $newToken");
      await LocalStorage.saveData(key: AppConstant.fcmToken, data: newToken);
    });

    // ‚úÖ Init local notifications
    const androidSettings = AndroidInitializationSettings('@mipmap/ic_launcher');
    const iosSettings = DarwinInitializationSettings();
    final initSettings = InitializationSettings(android: androidSettings, iOS: iosSettings);

    await flutterLocalNotificationsPlugin.initialize(
      initSettings,
      onDidReceiveNotificationResponse: (NotificationResponse response) async {
        if (response.payload != null) {
          print('üîó Notification payload: ${response.payload}');
        }
      },
    );

    // ‚úÖ Foreground notification handling
    FirebaseMessaging.onMessage.listen((RemoteMessage message) {
      print('üì¨ Foreground: ${message.notification?.title}');
      _showNotification(
        message.notification?.title,
        message.notification?.body,
      );
    });

    // ‚úÖ Background notification handler
    FirebaseMessaging.onBackgroundMessage(_firebaseMessagingBackgroundHandler);
  } catch (e) {
    print("‚ùå Firebase Messaging setup failed: $e");
  }
}

Future<void> _showNotification(String? title, String? body) async {
  const androidDetails = AndroidNotificationDetails(
    'default_channel',
    'General Notifications',
    importance: Importance.max,
    priority: Priority.high,
  );

  const iosDetails = DarwinNotificationDetails();

  const platformDetails = NotificationDetails(
    android: androidDetails,
    iOS: iosDetails,
  );

  await flutterLocalNotificationsPlugin.show(
    0,
    title ?? 'No Title',
    body ?? 'No Message',
    platformDetails,
  );
}
